package com.owners.gravitas.dto;

/**
 * The Class ClientStatisticsDTO.
 *
 * @author amits
 */
public class ClientStatisticsDTO {

    /** The duration. */
    private String duration;

    /** The call. */
    private String call;

    /** The email. */
    private String email;

    /** The sms. */
    private String sms;

    /** The total. */
    private String total;

    /**
     * Gets the duration.
     *
     * @return the duration
     */
    public String getDuration() {
        return duration;
    }

    /**
     * Sets the duration.
     *
     * @param duration
     *            the duration to set
     */
    public void setDuration( String duration ) {
        this.duration = duration;
    }

    /**
     * Gets the call.
     *
     * @return the call
     */
    public String getCall() {
        return call;
    }

    /**
     * Sets the call.
     *
     * @param call
     *            the call to set
     */
    public void setCall( String call ) {
        this.call = call;
    }

    /**
     * Gets the email.
     *
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * Sets the email.
     *
     * @param email
     *            the email to set
     */
    public void setEmail( String email ) {
        this.email = email;
    }

    /**
     * Gets the sms.
     *
     * @return the sms
     */
    public String getSms() {
        return sms;
    }

    /**
     * Sets the sms.
     *
     * @param sms
     *            the sms to set
     */
    public void setSms( String sms ) {
        this.sms = sms;
    }

    /**
     * Gets the total.
     *
     * @return the total
     */
    public String getTotal() {
        return total;
    }

    /**
     * Sets the total.
     *
     * @param total
     *            the total to set
     */
    public void setTotal( String total ) {
        this.total = total;
    }
}
